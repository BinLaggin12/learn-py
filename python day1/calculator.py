fir_number = input("first number:")
sec_number = input("second number:")
addition = float(fir_number) + float(sec_number)
subtraction = float(fir_number) - float(sec_number)
multiplication = float(fir_number) * float(sec_number)
division = float(fir_number) / float(sec_number)
print("sum:" + str(addition))
print("difference:" + str(subtraction))
print("product:" + str(multiplication))
print("quotient:" + str(division))



